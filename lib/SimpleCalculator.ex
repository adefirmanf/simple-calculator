defmodule SimpleCalculator do
  @moduledoc """
  Documentation for SimpleCalculator.
  """

  @doc """
  Module for simple calculator.

  ## Examples

      iex> SimpleCalculator.sum(1,2)
      3

  """
  def sum(a,b), do: a+b
  def min(a,b), do: a-b
  def multiply(a,b), do: a*b
  def divided(a,b), do: a/b
end
